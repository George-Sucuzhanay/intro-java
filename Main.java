// class is a container for  block of code.
public class Main {

    public static void main(String[]args) {

        System.out.println("Hello World");
        System.out.println("HELLO HELLO");

        // Types of variables: String for words, int for number
        // End your lines with a semicolon ;

        // The value of a string must be whithin double quote.
        String name = "Brian";
        System.out.println("Hi, my name is " + name);
        
        // for type int, we do not use quotes.
        int favoriteNumber = 2;
        System.out.println("My favorite number is " + favoriteNumber);
    
        System.out.println("Hi, my name is " + name + " and my favorite number is " + favoriteNumber);    
    }
}